package ejercicio4;

public class Operaciones {

	//Creacion de varibales globales
	int num1, num2;

	//Esta funcion se encarga de sumar los numeros
	int sum() {
		return num1 + num2;
	}
	
	//Esta funcion se encarga de restar los numeros
	int res() {
		return num1 - num2;
	}
	
	//Esta funcion se encarga de multiplicar los numeros
	double multi() {
		return num1 * num2;
	}
	
	//Esta funcion se encarga de dividir los numeros
	double div() {
		return num1 / num2;
	}
}
